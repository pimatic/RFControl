unsigned long micros(void) {
	static unsigned long duration = 0;
	if(sim_timings_pos < sim_timings_size) {
		duration += sim_timings[sim_timings_pos++];
		return duration;
	} else {
		printf("No timings left...\n");
	}
	return 0;
}


